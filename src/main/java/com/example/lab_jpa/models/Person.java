package com.example.lab_jpa.models;

import jakarta.persistence.*;

@Entity
@Table(name = "person")
public final class Person {

    @Id // esto le indica a nuestra app que es una PRIMARY key
    @GeneratedValue(strategy = GenerationType.IDENTITY) // esta anotaci√≥n es para indicar que es autoincremental.
    private int id;

    private String name;

    private int age;

    public Person() {
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
